# Exercício 30 – Par ou Ímpar?
numero = int(input('me diga um numero qualquer:'))
resultado = numero % 2
if resultado == 0:
    print('o numero {} é PAR'.format(numero))
else:
    print('o numero {} é IMPAR'.format(numero))


