# como tratar exceção em python

'''
Exceções sao erros que ocorrem dentro do programa, e essa exceção vai parar o programa,
por isso é importante tratá-las
'''
''' TRY=== tudo que estiver dentro do bloco try ele vai tentar executar, apresente algum erro ele vai pular para o bloco 
except,
quando o código sintáticamente correto se depara com um erro, o Python lançará um erro de exceção.
este erro de exceção irá travar o programa se ele não for manipulado. 

'''

''' except === pode ser manipulado como quiser, podemos mandar uma mensagem informativa
como o tipo do erro que o programa esta apresentando,
assim podemos tratar o erro, 'com o erro resolvido o sistema volta a funcionar normalmente' .

'''

'''
para tratar uma exceção é só seguir os exemplos a baixo 
'''


try:
    print()
except:
    pass
