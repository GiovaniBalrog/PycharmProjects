# Definindo a temperatura
temperatura = int(input("Quantos graus esta fazendo hoje? :"))  # Solicita ao usuário que insira a temperatura atual.

# Verificando a temperatura usando um if
if temperatura > 30:
    print("Está quente!")  # Se a temperatura for maior que 30, imprime "Está quente!"
else:
    print("Está agradável.")  # Se a temperatura não for maior que 30, imprime "Está agradável."
